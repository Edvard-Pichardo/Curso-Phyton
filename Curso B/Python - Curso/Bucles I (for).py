#Bucles I (for)

#Su utilidad es repetir varias lineas de código, varias veces.

#Pueden ser determinados: Se ejecuta un número determinado de veces y se puede leer en el código
#Indeterminado: no se puede saber cuantás veces se va a repetir el bucle

#Sintaxis

# for variable in elemento_a_recorrer:
#     cuerpo del bucle

#elemento a recorrer: lista, tuplas, cadenas de texto, rango, etc.


for i in [1,2,3]:
	print("Hola, acabas de ser infectado por un virus")
	print(i)

for j in ["Primavera", "Verano", "Otoño", "Invierno"]:
	print("Hola, acabas de ser infectado por un virus, otra vez. xd")
	print(j)

