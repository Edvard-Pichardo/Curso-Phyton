#Formas de programar: 
#Orientados a procedimientos: Lenguajes antiguos (80, 60)
#Orientados a Objetos: Lenguajes Modernos 

#Programación Orientados a Procedimientos
#Ejemplos: Fortram, Cobol, Basic, etc.
#Desventajas: 
# Unidades de código muy grandes en aplicaciones complejas
# En aplicaciones complejas el mcódigo resulta díficil de descifrar
# Poco reutilizable
# Si existe una falla en una línea de código, es muy probable que el programa caiga
# Aparición frecuente de código spaguetti (Da saltos hacia arriba/abajo)
# Díficil de depurar por otros programadores en caso de necesidad o error

#Programación Orientada a Objetos
#Consiste en trasladar el comportamiento de los objetos de la vida real al código de programación
#Es decir, tienen un estado, un comportamiento (¿Qué puede hacer?) y propiedades

#Ejemplo. Objeto: Coche
# ¿Cuál es el estado del coche? Puede estar detenido, circulando, aparcado, etc.
# ¿Qué propiedades tiene un coche? Un coche puede tener un color, un peso, un tamaño, etc.
# ¿Qué comportamiento puede tener? Puede arrancar, frenar, girar, acelerar, etc.

#Resumen:
#  Objeto:
#     Tiene propiedades (atributos)
#     Tiene un comportamiento (Lo que es capaz de hacer)

#Ejemplos: C++. Java, Visual.NET, etc.
#Ventajas:
# Programas divididos en trozos, partes o modulos. (modularización)
# Son reutilizables. (Herencia)
# Si existe un fallo en alguna línea de código, el programa continuará con su funcionamiento. (Tratamiento de excepciones)
