#Conceptos explicados:
#1. Clase: Modelo donde se redactan las caracteristicas comunes de un grupo/conjunto de objetos. 
#2. Objeto: Ejemplar o instancia que pertenecen a una clase con caracteristicas únicas.
#3. Ejemplarizar una clase, instanciar una clase.
#4. Modularización: Una aplicación compuesta puede estar compuesta por varias clases.
#5. Encapsulamiento/encapsulación: El funcionamiento interno de cada clase solo se sabe dentro de dicha clase y no fuerade ella.
#6. Herencia: Reutilizar partes o códigos completos.
#7. Polimorfismo.


