#Programación Orientada a Objetos: C++, Java, Visual NET, etc.
#Consiste en trasladar comportamientos deobjetos reales al código de programación.
#Los objetos tienen un estado, un comportamiento (lo que es capaz de hacer) y unas propiedades (atributos).
#Programas dividisos en trozos/partes/modulos/clases.
#El código es muy reutilizable y utiliza HERENCIA.
#Si existe algún fallo en alguna línea del código, el programa continuará con su funcionamiento si tratamos los errores.
#El encapsulamiento.


